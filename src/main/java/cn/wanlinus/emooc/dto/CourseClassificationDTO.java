package cn.wanlinus.emooc.dto;

import java.io.Serializable;

/**
 * @author wanli
 * @date 2018-05-05 16:04
 */
public class CourseClassificationDTO implements Serializable {
    private String id;
    private String name;
    private Boolean flag;

    public CourseClassificationDTO() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getFlag() {
        return flag;
    }

    public void setFlag(Boolean flag) {
        this.flag = flag;
    }

    @Override
    public String toString() {
        return "CourseClassificationDTO{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", flag=" + flag +
                '}';
    }
}
