package cn.wanlinus.emooc.dto;

import java.io.Serializable;
import java.util.List;

/**
 * 使用Bootstrap分页插件所使用的数据传输对象
 *
 * @author wanli
 * @date 2018-05-28 14:00
 */
public class BootstrapPaginationDataDTO<T> implements Serializable {
    private Integer currentPage;
    private Integer prePage;
    private Integer nextPage;
    private Integer totalPage;
    private List<T> data;

    public BootstrapPaginationDataDTO() {
    }

    public Integer getCurrentPage() {
        return currentPage;
    }

    public void setCurrentPage(Integer currentPage) {
        this.currentPage = currentPage;
    }

    public Integer getPrePage() {
        return prePage;
    }

    public void setPrePage(Integer prePage) {
        this.prePage = prePage;
    }

    public Integer getNextPage() {
        return nextPage;
    }

    public void setNextPage(Integer nextPage) {
        this.nextPage = nextPage;
    }

    public Integer getTotalPage() {
        return totalPage;
    }

    public void setTotalPage(Integer totalPage) {
        this.totalPage = totalPage;
    }

    public List<T> getData() {
        return data;
    }

    public void setData(List<T> data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "BootstrapPaginationDataDTO{" +
                "currentPage=" + currentPage +
                ", prePage=" + prePage +
                ", nextPage=" + nextPage +
                ", totalPage=" + totalPage +
                ", data=" + data +
                '}';
    }
}
